import mongoose from "mongoose";

const documentSchema = new mongoose.Schema(
  {
    url: {
      type: String,
      required: true,
    },
    filename: {
      type: String,
      required: true,
    },
    size: {
      type: Number,
      required: true,
    },
    format: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Document =
  mongoose.models.Document || mongoose.model("Document", documentSchema);
export { Document };
